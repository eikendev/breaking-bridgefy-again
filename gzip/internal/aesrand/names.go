package aesrand

func (r *AesRand) choose128() int {
	buffer := make([]byte, 16)
	r.Read(buffer)
	idx := buffer[0]
	idx &= 0b01111111
	return int(idx)
}

func (r *AesRand) GetRandomName() string {
	idx := r.choose128()
	return names[idx]
}

